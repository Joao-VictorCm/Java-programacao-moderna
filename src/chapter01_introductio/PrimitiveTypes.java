package chapter01_introductio;

public class PrimitiveTypes {

	public static void main(String[] args) {

        byte age = 25;
        short year = 2025;
        int population = 1000000;
        long distance = 9000000000L;

        float height = 1.75f;
        double salary = 3500.50;

        char gender = 'M';
        boolean isStudent = true;

        System.out.println("Idade: " + age);
        System.out.println("Ano: " + year);
        System.out.println("População: " + population);
        System.out.println("Distância: " + distance);
        System.out.println("Altura: " + height);
        System.out.println("Salário: " + salary);
        System.out.println("Gênero: " + gender);
        System.out.println("É estudante? " + isStudent);

	}

}
